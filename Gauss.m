function [ksi,w] = Gauss(n)
%fonction de quadrature de Gauss en 1D
if (n==1)
    ksi(1)= 0;
    w(1)=2;
end

if (n==2)
    ksi(1,1)= -1/sqrt(3);ksi(2,1)= 1/sqrt(3);
    w(1,1)=1; w(2,1)=1;
end

if (n==3)
    ksi(1,1)= 0;ksi(2,1)= -sqrt(3/5);ksi(3,1)= sqrt(3/5);
    w(1,1)=8/9; w(2,1)=5/9;w(3,1)=5/9;
end
if (n==4)
    ksi(1,1)=  sqrt( (3- 2* sqrt(6/5))/7) ; ksi(2,1)= - sqrt( (3- 2* sqrt(6/5))/7);
    ksi(3,1)=  sqrt( (3+ 2* sqrt(6/5))/7) ; ksi(4,1)= - sqrt( (3+ 2* sqrt(6/5))/7);
    
    w(1,1)= 1/2 + (1/6) * 1/sqrt(6/5); w(2,1)=1/2 + (1/6) * 1/sqrt(6/5);
    w(3,1)= 1/2 - (1/6) * 1/sqrt(6/5); w(4,1)= 1/2 - (1/6) * 1/sqrt(6/5);
end
if (n==7)
    ksi(1,1) = 0; ksi(2,1)=0.405845151377397;
    ksi(3,1)= -0.405845151377397; ksi(4,1)=0.741531185599394;
    ksi(5,1)= -0.741531185599394; ksi(6,1)= 0.949107912342759;
    ksi(7,1)= -0.949107912342759;
    
    w(1,1)=0.417959183673489; w(2,1)=0.381830050505119;
    w(3,1)=0.381830050505119; w(4,1)=0.279705391489277;
    w(5,1)=0.279705391489277; w(6,1)=0.129484966168870;
    w(7,1)=0.129484966168870;
end
end

